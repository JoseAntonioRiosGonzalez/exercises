public class ForLoop {

    public static void main(String[] args){

        //1-starting condition
        //2-stop condition
        //3-step condition
        for (int i =0; i <=10; i++){//normally we use for loop when we already know
        //how many iterations there will be (this is called a determined control structure)
            System.out.println(i);
        }
    }
}
